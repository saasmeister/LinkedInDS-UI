#!/usr/bin/env node
// Build components/icons/icon-library.js from a folder of SVGs. Each icon is
// embedded as a raw SVG string and installed via IconKit.installSvg(), which
// converts it to editable anchors AT RUNTIME — so the converter lives only in
// icon-kit.js (always imported), and this script is a trivial bundler.
//
//   node tools/icons/build-icon-library.mjs <svg-dir> --accent "#F2685C" --out components/icons/icon-library.js
//
// NOTE: you don't strictly need this script — an agent can write the same
// IconKit.installSvg('name', '<svg…>', {accent}) calls directly. It just saves typing.
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, basename } from "node:path";
const args = process.argv.slice(2);
const dir = args.find((a) => !a.startsWith("--"));
const opt = (k, d) => { const i = args.indexOf(k); return i >= 0 && args[i + 1] ? args[i + 1] : d; };
if (!dir) { console.error("usage: build-icon-library.mjs <svg-dir> [--accent #hex] [--out file]"); process.exit(1); }
const accent = opt("--accent", "");
const out = opt("--out", join(dir, "icon-library.js"));
const files = readdirSync(dir).filter((f) => f.toLowerCase().endsWith(".svg")).sort();
const lines = [
  "// GENERATED by tools/icons/build-icon-library.mjs — load AFTER icon-kit.js.",
  "(function () {",
  "  if (!window.IconKit || !IconKit.installSvg) { console.warn('icon-library: IconKit.installSvg missing — load icon-kit.js first'); return; }",
  "  var A = " + JSON.stringify(accent ? { accent } : {}) + ";",
];
let ok = 0;
for (const f of files) {
  const name = basename(f, ".svg");
  const svg = readFileSync(join(dir, f), "utf8").replace(/\s+/g, " ").trim();
  lines.push("  IconKit.installSvg(" + JSON.stringify(name) + ", " + JSON.stringify(svg) + ", A);");
  ok++;
}
lines.push("})();", "");
writeFileSync(out, lines.join("\n"));
console.log(`Wrote ${out} — ${ok} icons.`);
